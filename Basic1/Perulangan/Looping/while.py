'''
while
Struktur while pada python tidak berbeda jauh dengan 
bahasa pemrograman lainnya. Lebih sederhananya, 
struktur penulisan python membuat dia mudah untuk dibaca.
'''


j = 0 #nilai awal j =0


while j<7: #ketika j kurang dari 7 lakukan perulangan, jika tidak stop perulangan
    
    print("Ini adalah perulangan ke -",j) #lakukan perintah ini ketika perulangan
    
    j=j+1 #setiap kali diakhir perulangan update nilai dengan ditambah 1.